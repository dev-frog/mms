    {
        //
        $point = new Point();
        $point->shopping = $request->shopping;
        $point->point = floor($request->shopping/100);
        $member = Member::findOrFail($request->member_id);
        $member->point()->save($point);
        return redirect()->back()->with('message','Point add successfully');


